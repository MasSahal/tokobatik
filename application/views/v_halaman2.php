              <p>Batik tersebut merupakan khas Cirebon yang memiliki daya tarik tersendiri. Batik Trusmi termasuk dalam kategori batik pesisir karena lokasi geografis Kota Cirebon. Kata batik sendiri berasal dari bahasa Jawa. 
                    Secara etimologi kata batik diambil dari kata ambatik yaitu gabungan dari kata amba dalam bahasa Jawa yang berarti menulis dan tik yang berarti titik kecil, tetesan, atau membuat titik. Jadi, batik adalah menulis atau melukis titik. 
                    Kampung Batik Trusmi merupakan jantung produksi batik Cirebon. Ada lebih dari seribu perajin batik yang bekerja setiap hari. 
                    Tak hanya penduduk Desa Trusmi, orang-orang dari daerah sekitar seperti Gamel, Kaliwulu, Wotgali, dan Kalitengah turut berkontribusi dalam pembuatan Batik Trusmi. Pada jalan sepanjang 1,5 kilometer di Kampung Batik Trusmi terdapat deretan toko dan ruang pameran yang menampilkan produk-produk terbaik para perajin.</p>
